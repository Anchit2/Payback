package com.example.payback;

public class Listvalues {
    String date,forr,amount;

    public Listvalues(String date, String forr, String amount) {
        this.date = date;
        this.forr = forr;
        this.amount = amount;
    }

    public String getDate() {
        return date;
    }



    public String getForr() {
        return forr;
    }


    public String getAmount() {
        return amount;
    }


}
